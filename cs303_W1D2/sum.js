/**
 * Returns sum of numbers in the array.
 *
 * @param {object} arr input array.
 * @return {number} sum of numbers in the array.
 */

function sum(arr) {
    debugger;
    let sum = 0;
    for (let i = 0; i < arr.length; i++) {
        sum += arr[i]
    }
    return sum;
}

